package String;

public class Concatenation {
    public static void main(String[] args) {
        String FirstName = "Ebrahim";
        String LastName = "Iqbal Khan";
        String FullName = FirstName + " " + LastName;
        System.out.println(FullName);
        int age = 22;
        String str = age + "";
        System.out.println(str);

    }
}
